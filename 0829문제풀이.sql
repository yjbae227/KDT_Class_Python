-- 1. 30번 부서의 직무별 급여 평균을 파악하고자 해요 
-- 			출력 컬럼명은 직무(JOB), 급여 평균(SAL) 으로 하고 급여 평균은 백 단위까지만(십단위이하 절사) 나오게 출력하세요 
SELECT JOB, ROUND(AVG(SAL), -2)
	FROM EMP 
    WHERE DEPTNO = 30 
    GROUP BY JOB; 
    
    
    
    
-- 2. 근무 중인 직원이 4명 이상인 부서의 정보를 출력하세요 
-- 부서의 정보토 출력해야 하고 직원에 대한 정보는 emp에 있으니 join이 필요하겠네여 

SELECT D.DNAME AS '부서명',
		COUNT(E.EMPNO) AS '직원수'
	FROM DEPT D
		INNER JOIN EMP E 
		ON D.DEPTNO = E.DEPTNO
	GROUP BY D.DNAME
    HAVING COUNT(E.EMPNO) >= 4; 
    
    
